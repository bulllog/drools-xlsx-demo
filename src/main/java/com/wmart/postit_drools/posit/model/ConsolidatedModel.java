package com.wmart.postit_drools.posit.model;

public class ConsolidatedModel {
	
	private String drools;
	private String transformation_rules;
	private String event;
	
	public String getDrools() {
		return drools;
	}
	public void setDrools(String drools) {
		this.drools = drools;
	}
	public String getTransformation_rules() {
		return transformation_rules;
	}
	public void setTransformation_rules(String transformation_rules) {
		this.transformation_rules = transformation_rules;
	}
	public String getEvent() {
		return event;
	}
	public void setEvent(String event) {
		this.event = event;
	}
}
